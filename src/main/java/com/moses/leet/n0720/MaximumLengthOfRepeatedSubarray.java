    public int findLengthSlowest(int[] A, int[] B) {
        int max = 0;
        for(int i=0; i<A.length; i++){
            for(int j=0; j<B.length; j++){
                if(A[i] == B[j]){
                    int m = i, n = j, cnt = 0;
                    while(m<A.length && n<B.length && A[m++] == B[n++]){
                        cnt++;
                    }
                    if(cnt > max){
                        max = cnt;
                    }
                }
            }
        }
        return max;
    }
